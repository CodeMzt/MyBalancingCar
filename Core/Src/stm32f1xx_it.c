/**
  * @brief This function handles USART3 global interrupt.
  */
void USART3_IRQHandler(void)
{
  /* USER CODE BEGIN USART3_IRQn 0 */

  /* USER CODE END USART3_IRQn 0 */
  HAL_UART_IRQHandler(&huart3);
  /* USER CODE BEGIN USART3_IRQn 1 */
	HAL_UART_Receive_IT(&huart3,rx_buf,1);
	/**
	G: go forward
	K: go back
	I: stop
	J: turn right
	H: turn left
	S: stop turn
	*/
	switch(rx_buf[0]){
		case 'G':
			orientation[0] = 1;
			break;
		case 'K':
			orientation[0] = 0;
			orientation[1] = 1;
			break;
		case 'I':
			orientation[0] = 0;
			orientation[1] = 0;
			break;
		case 'J':
			orientation[2] = 0;
			orientation[3] = 1;
			break;
		case 'H':
			orientation[2] = 1;
			orientation[3] = 0;
			break;
		case 'S':
			orientation[2] = 0;
			orientation[3] = 0;
			break;
		default: 
			orientation[0] = 0;
			orientation[1] = 0;
			orientation[2] = 0;
			orientation[3] = 0;
	}
  /* USER CODE END USART3_IRQn 1 */
}

